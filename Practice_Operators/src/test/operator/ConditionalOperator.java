//conditionalOperator is also known as ternary operator because it works with three operands.

package test.operator;

public class ConditionalOperator {
	public static void main(String[] args) {
		int a,b;
		a=20;
		
		b=(a==1)?30:40;
		System.out.println("Value of b :"+b);
		
		b=(a==20)?30:40;
		System.out.println("Value of b :"+b);
		
	}

}
