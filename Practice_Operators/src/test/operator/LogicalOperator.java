//Logical Opeator is used to check conditional operation

package test.operator;

public class LogicalOperator {
	public static void main(String[] args) {
		boolean a=false;
		boolean b=true;
		
		System.out.println("a && b :" +(a&&b));
		System.out.println("a || b :" +(a||b));
		System.out.println("!(a && b) :"+ !(a && b));
	}

}
