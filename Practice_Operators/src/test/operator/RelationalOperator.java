//Relational Operator is used to comparision between two operands

package test.operator;

public class RelationalOperator {
	public static void main(String[] args) {
		int a=40;
		int b=30;
		
		System.out.println("a == b :"+(a==b));
		System.out.println("a != b :"+(a!=b));
		System.out.println("a > b :"+ (a>b));
		System.out.println("a < b :"+ (a<b));
		System.out.println("a >= b :"+ (a>=b));
		System.out.println("a < b :"+ (a<=b));
	}

}
